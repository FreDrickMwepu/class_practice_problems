#include <iostream>
using namespace std;

int main() {

    // For Loop Syntax 
    for (int i = 0; i <= 100; i++){
        cout<<i<<endl;
    }
    // While Loop Syntax
    int i = 0;
    while (i<=0) {
        cout << i <<endl;
        i++;
    }
    // Do while loop Syntax
    int i = 0;
    do{
        cout <<i<< endl;
        i++;
    } while(i<=10);

    return 0;
}